        let cell = tableView.dequeueReusableCell(withIdentifier: "cargoCell", for: indexPath)
        
        cell.textLabel?.text = cargos[indexPath.row]

        return cell
    }
    
    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        
        titulo = cargos[indexPath.row]
        
        indice = indexPath.row
        
        self.performSegue(withIdentifier: "cargosToCargoSelecionado", sender: self)
        
        tableView.deselectRow(at: indexPath, animated: true)
        
    }
    
    
